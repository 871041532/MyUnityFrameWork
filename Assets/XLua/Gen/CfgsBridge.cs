using XLua;
using System;


namespace XLua.CSObjectWrap
{
    public class CfgsBridge : LuaBase, Cfgs
    {
	    public static LuaBase __Create(int reference, LuaEnv luaenv)
		{
		    return new CfgsBridge(reference, luaenv);
		}
		
		public CfgsBridge(int reference, LuaEnv luaenv) : base(reference, luaenv)
        {
        }
		
        

        
        System.Collections.Generic.Dictionary<int, ItemCfg> Cfgs.itemCfgs 
        {
            
            get 
            {
#if THREAD_SAFE || HOTFIX_ENABLE
                lock (luaEnv.luaEnvLock)
                {
#endif
					RealStatePtr L = luaEnv.L;
					int oldTop = LuaAPI.lua_gettop(L);
					ObjectTranslator translator = luaEnv.translator;
					LuaAPI.lua_getref(L, luaReference);
					LuaAPI.xlua_pushasciistring(L, "itemCfgs");
					if (0 != LuaAPI.xlua_pgettable(L, -2))
					{
						luaEnv.ThrowExceptionFromError(oldTop);
					}
					System.Collections.Generic.Dictionary<int, ItemCfg> __gen_ret = (System.Collections.Generic.Dictionary<int, ItemCfg>)translator.GetObject(L, -1, typeof(System.Collections.Generic.Dictionary<int, ItemCfg>));
					LuaAPI.lua_pop(L, 2);
					return __gen_ret;
#if THREAD_SAFE || HOTFIX_ENABLE
                }
#endif
            }
            
            
            set
            {
#if THREAD_SAFE || HOTFIX_ENABLE
                lock (luaEnv.luaEnvLock)
                {
#endif
					RealStatePtr L = luaEnv.L;
					int oldTop = LuaAPI.lua_gettop(L);
					ObjectTranslator translator = luaEnv.translator;
					LuaAPI.lua_getref(L, luaReference);
					LuaAPI.xlua_pushasciistring(L, "itemCfgs");
					translator.Push(L, value);
					if (0 != LuaAPI.xlua_psettable(L, -3))
					{
						luaEnv.ThrowExceptionFromError(oldTop);
					}
					LuaAPI.lua_pop(L, 1);
#if THREAD_SAFE || HOTFIX_ENABLE
                }
#endif
            }
            
        }
        
        
        
		
		
	}
}
